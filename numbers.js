// Example of working with numbers in JavaScript

// Creating variables to store numbers
var number1 = 10;
var number2 = 5;

// Addition
var sum = number1 + number2;
console.log("Sum of numbers: " + sum); // Output the result to the console

// Subtraction
var difference = number1 - number2;
console.log("Difference of numbers: " + difference); // Output the result to the console

// Multiplication
var product = number1 * number2;
console.log("Product of numbers: " + product); // Output the result to the console

// Division
var quotient = number1 / number2;
console.log("Quotient of numbers: " + quotient); // Output the result to the console

// Remainder
var remainder = number1 % number2;
console.log("Remainder of division of numbers: " + remainder); // Output the result to the console

// Exponentiation
var power = Math.pow(number1, number2);
console.log("Number " + number1 + " to the power of " + number2 + " equals: " + power); // Output the result to the console
